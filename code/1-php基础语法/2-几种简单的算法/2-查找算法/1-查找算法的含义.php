2：二分查找算法
二分查找要求线性表中的节点按关键字升序或降序排列，用给定值k与中间节点的关键字比较，中间点将线性表分成2个子表
若相等则查找成功，若不相等，再根据k与中间节点关键字的比较结果，确定下一步查找哪个子表，递归进行，直到查找成功或发现表中没有能匹配的节点

