  /**
   * 3 foreach 遍历原理
   * 本质就是数组内部有一个指针默认指向第一个元素，通过指针下移来获取下一个，循环，直到取到最后
   * 执行过程：
   * 1) foreach 会重置指针，让指针指向第一个元素
   * 2) 指针访问指向的元素所在内存空间，将$key指向元素的下标，$value指向该元素值，然后指针下移
   * 3) 执行循环体
   * 4) 重复2和3，直到指针取不到数组元素，数组遍历完成
